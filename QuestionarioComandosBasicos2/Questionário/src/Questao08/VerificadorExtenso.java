package Questao08;

import java.util.Scanner;

public class VerificadorExtenso {
	public static void main (String[] args) {
		int numero ;
		System.out.println("Digite o numero que quer vê por extenso");
		Scanner ler = new Scanner (System.in);
		numero = ler.nextInt();
		switch (numero) {
		case 1:
			System.out.println("O numero digitado se chama -UM-");
			break;
		case 2:
			System.out.println("O numero digitado se chama -DOIS-");
			break;
		case 3:
			System.out.println("O numero digitado se chama -TRES-");
			break;
		case 4:
			System.out.println("O numero digitado se chama -QUATRO-");
			break;
		case 5:
			System.out.println("O numero digitado se chama -CINCO-");
			break;
		case 6:
			System.out.println("O numero digitado se chama -SEIS-");
			break;
		case 7:
			System.out.println("O numero digitado se chama -SETE-");
			break;
		case 8:
			System.out.println("O numero digitado se chama -OITO-");
			break;
		case 9:
			System.out.println("O numero digitado se chama -NOVE-");
			break;
		case 10:
			System.out.println("O numero digitado se chama -DEZ-");
			break;
			
		}
		
		
	}

}
